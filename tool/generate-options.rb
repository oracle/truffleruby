require 'ostruct'
require 'yaml'
require 'erb'


def camelize(string)
  string.sub(/^([a-z\d]*)/) { $1.capitalize }.gsub(/_([a-z\d]*)/) { $1.capitalize }
end

options_data = YAML.load_file('tool/options.yml')

options = options_data.map do |constant, ((name, *mri_names), type, default, description, *rest)|

  raise "More than 5 arguments in #{constant}" unless rest.empty?

  case type
  when 'boolean'
    type       = 'boolean'
    boxed_type = 'Boolean'
    default    = default.to_s
  when 'integer'
    type       = 'int'
    boxed_type = 'Integer'
    default    = default.to_s
  when /^enum\/(\w*)/
    name       = $1
    type       = camelize name
    boxed_type = type
    default    = "#{type}.#{default.to_s.upcase}"
  when 'string'
    type       = 'String'
    boxed_type = type
    default    = default.nil? ? 'null' : "\"#{default.to_s}\""
  when 'string-array'
    type             = 'String[]'
    boxed_type       = type
    type_description = 'StringArrayOptionDescription'
    default          = "new String[]{#{default.map(&:inspect).join(', ')}}"
  else
    raise type.to_s
  end

  OpenStruct.new(
      constant:          constant,
      name:              name,
      mri_names:         mri_names,
      type:              type,
      type_description:  type_description || "#{boxed_type}OptionDescription",
      boxed_type:        boxed_type,
      default:           default,
      reference_default: /^[A-Z_]+$/.match(default),
      description:       description + (mri_names.empty? ?
                                            '' : " (configured by #{mri_names.join(', ')} Ruby options)")
  )
end

